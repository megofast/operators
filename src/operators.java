public class operators {
    public static void main(String args[]) {
        System.out.println("2 + 4 = " + (2 + 4));
        System.out.println("7 - 4 = " + (7 - 4));
        System.out.println("9 * 4 = " + (9 * 4));
        System.out.println("8 / 2 = " + (8 / 2));
        System.out.println("5 % 4 = " + (5 % 4));
    }
}
